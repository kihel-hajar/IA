fig, ax = plt.subplots(figsize=(14, 6))

for i, (nom, r) in enumerate(resultats.items()):
    valeurs = [r['accuracy'], r['precision'], r['recall'], r['f1'], r['auc_roc']]
    ax.bar(x + i * largeur, valeurs, largeur, label=nom, color=couleurs[i], alpha=0.85)

