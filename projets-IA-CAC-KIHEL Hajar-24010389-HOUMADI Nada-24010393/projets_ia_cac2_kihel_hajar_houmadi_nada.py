ax.set_title(f'Matrice de Confusion - {meilleur_nom}', fontsize=13, fontweight='bold')
plt.tight_layout()
plt.show()

print('\nRapport de classification complet :')
print(classification_report(y_test, meilleur['y_pred'], target_names=['Reste', 'Depart']))

